    public ModelCamera parseJsonData(String jsonString){
        ModelCamera mc = new ModelCamera();
        try{
            JSONObject json = new JSONObject(jsonString);
            String speed = json.getString("speed");
            String f = json.getString("f");
            String iso = json.getString("iso");
            mc.setfStop(Double.parseDouble(f));
            mc.setShatterSpeed(Integer.parseInt(speed));
            mc.setIso(Integer.parseInt(iso));
        }catch (JSONException e){
            e.printStackTrace();
        }
        return mc;
    }

